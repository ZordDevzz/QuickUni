CREATE TYPE "enum_account_type" AS ENUM (
	'employee',
	'student',
	'dev'
);

CREATE TYPE "enum_gender" AS ENUM (
	'male',
	'female',
	'other'
);

CREATE TYPE "enum_schedule_type" AS ENUM (
	'regular',
	'substitute',
	'off',
	'makeup'
);

CREATE TABLE IF NOT EXISTS "account" (
	"id" UUID NOT NULL UNIQUE,
	"username" VARCHAR(255) NOT NULL UNIQUE,
	"pwd" VARCHAR(64) NOT NULL,
	"type" ENUM_ACCOUNT_TYPE,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "employee" (
	"id" VARCHAR(30) NOT NULL UNIQUE,
	"account_id" UUID,
	"fullname" VARCHAR(255) NOT NULL,
	"gender" ENUM_GENDER NOT NULL,
	"dob" DATE,
	"role_id" VARCHAR(30) NOT NULL,
	"address" VARCHAR(512) NOT NULL,
	"country_code" VARCHAR(2) NOT NULL DEFAULT 'VN',
	"national_id" VARCHAR(255),
	"department_id" VARCHAR(30) NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "student" (
	"id" VARCHAR(30) NOT NULL UNIQUE,
	"account_id" UUID,
	"fullname" VARCHAR(255) NOT NULL,
	"gender" ENUM_GENDER NOT NULL,
	"dob" DATE,
	"address" VARCHAR(512),
	"country_code" VARCHAR(2) DEFAULT 'VN',
	"national_id" VARCHAR(255),
	"main_class_id" VARCHAR(30),
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "department" (
	"id" VARCHAR(30) NOT NULL UNIQUE,
	"name" VARCHAR(255) NOT NULL,
	"head" VARCHAR(30) NOT NULL,
	"des" VARCHAR(512),
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "main_class" (
	"id" VARCHAR(30) NOT NULL UNIQUE,
	"monitor" VARCHAR(30),
	"teacher" VARCHAR(30) NOT NULL,
	"major_id" VARCHAR(30) NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "course_class" (
	"id" VARCHAR(30) NOT NULL UNIQUE,
	"subject_id" VARCHAR(30) NOT NULL,
	"teacher_id" VARCHAR(30) NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "subject" (
	"id" VARCHAR(30) NOT NULL UNIQUE,
	"name" VARCHAR(255) NOT NULL,
	"credits" SMALLINT NOT NULL,
	"major_id" VARCHAR(30) NOT NULL,
	"des" VARCHAR(255),
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "enrollment" (
	"id" INTEGER NOT NULL UNIQUE GENERATED BY DEFAULT AS IDENTITY,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "message" (
	"id" INTEGER NOT NULL UNIQUE GENERATED BY DEFAULT AS IDENTITY,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "target" (
	"id" INTEGER NOT NULL UNIQUE GENERATED BY DEFAULT AS IDENTITY,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "entities_group" (
	"id" INTEGER NOT NULL UNIQUE GENERATED BY DEFAULT AS IDENTITY,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "role" (
	"id" VARCHAR(30) NOT NULL UNIQUE,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "authority" (
	"id" INTEGER NOT NULL UNIQUE GENERATED BY DEFAULT AS IDENTITY,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "schedule" (
	"id" BIGINT NOT NULL UNIQUE GENERATED BY DEFAULT AS IDENTITY,
	"type" ENUM_SCHEDULE_TYPE NOT NULL,
	"schedule_date" DATE NOT NULL,
	"course_class_id" VARCHAR(30) NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "major" (
	"id" VARCHAR(30) NOT NULL UNIQUE,
	"des" VARCHAR(512),
	"department_id" VARCHAR(30) NOT NULL,
	PRIMARY KEY("id")
);



